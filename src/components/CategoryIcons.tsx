// 8-bit pixel art illustrations using SVG rects on a 16x16 grid

const S = 14; // pixel size in SVG units

function P({ x, y, c }: { x: number; y: number; c: string }) {
  return <rect x={x * S} y={y * S} width={S} height={S} fill={c} />;
}

